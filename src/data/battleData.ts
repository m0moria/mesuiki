export function getRandomRival(turn: number): RivalData {
  // ì´ˆë°˜(1~12): ì•½ì²´ ìœ„ì£¼, ì¤‘ë°˜(13~24): ì¤‘ê¸‰ í˜¼í•©, í›„ë°˜(25~): ê°•ì  ì¶œí˜„
  const weights = freeMatchRivals.map(rival => {
    if (rival.power <= 800) {
      // ì•½ì²´: ì´ˆë°˜ì— ë†’ì€ ê°€ì¤‘ì¹˜
      return turn <= 12 ? 10 : turn <= 24 ? 5 : 2
    } else if (rival.power <= 1800) {
      // ì¤‘ê¸‰: ì¤‘ë°˜ì— ë†’ì€ ê°€ì¤‘ì¹˜
      return turn <= 12 ? 1 : turn <= 24 ? 8 : 5
    } else {
      // ê°•ì : í›„ë°˜ì—ë§Œ ë“±ì¥
      return turn <= 12 ? 0 : turn <= 24 ? 1 : 6
    }
  })

  const totalWeight = weights.reduce((a: number, b: number) => a + b, 0 as number)
  let roll = Math.random() * totalWeight
  for (let i = 0; i < freeMatchRivals.length; i++) {
    roll -= weights[i]
    if (roll <= 0) return freeMatchRivals[i]
  }
  return freeMatchRivals[0]
}

