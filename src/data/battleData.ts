export function getAutoNarration(pleasure: number, satisfaction: number): string {
  const intensity = Math.max(pleasure, satisfaction)
  if (intensity >= 60) {
    return autoNarrations.high[Math.floor(Math.random() * autoNarrations.high.length)]
  }
  if (intensity >= 30) {
    return autoNarrations.mid[Math.floor(Math.random() * autoNarrations.mid.length)]
  }
  return autoNarrations.low[Math.floor(Math.random() * autoNarrations.low.length)]
}

